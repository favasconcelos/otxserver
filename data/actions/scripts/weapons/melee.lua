  for item, trash in pairs(items) do
    if item2.itemid == item then
      if chance >= 7 then
        doTransformItem(item2.uid, trash)
      end
    end
  end

  if item2.itemid == 7537 then
    doTransformItem(item2.uid, 7536)
    doSummonCreature("Spider", topos)
  elseif item2.itemid == 7585 then
    doTransformItem(item2.uid, 7586)
    doSummonCreature("Giant Spider", topos)
  end

  doSendMagicEffect(topos, CONST_ME_POFF)
  return TRUE
end
